                                            <p style="font-size: 14px; color: rgb(0, 0, 0); font-family: Geneva, Arial, Helvetica, sans-serif;">
&nbsp; &nbsp; &nbsp;บ้านธาตุจอมศรีนี้ เดิมคงสืบเชื้อสายมาจาก ชาวแขวงเมืองหลวงพระบาง แขวงเมืองล้านช้าง และแขวงเมืองไชยบุรี ทั้งนี้เพราะประเพณีขนบธรรมเนียมความเป็นอยู่ ตลอดจนสำเนียงภาษาก็คล้ายคลึงกันมาก การก่อตั้งบ้านสมัยแรก ๆ นั้นไม่มีหลักฐานพอจะอ้างอิงเป็นที่แน่นอนได้ เพราะสมัยนั้นท้องถิ่นเต็มไปด้วยป่าเขา ผู้คนอยู่กันเป็นหย่อม ๆ เสมือนกับการทำไร่แถว ๆ ปากช่อง ตอนแรกนั้นผู้คนก็คงจะไม่ได้รับการศึกษา จึงไม่มีประวัติจารึกเป็นหลักฐานไว้ เพียงแต่เล่าสู่กันฟังต่อ ๆ กันมา อาจจะขาด ๆ ตก ๆ ไปบ้าง แต่อาศัยการสันนิษฐานจากซากวัตถุและโบราณวัตถุที่ยังคงเหลือออยู่เท่านั้น</p>
<p style="font-size: 14px; color: rgb(0, 0, 0); font-family: Geneva, Arial, Helvetica, sans-serif;">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;บริเวณที่ตั้งหลักแหล่งเดิม คงจะไม่ใช่ที่ตั้งหมู่บ้านธาตุที่อยู่ในปัจจุบันนี้ เดิมทีเดียวหมู่บ้านนี้มิได้เรียกชื่อว่า บ้านธาตุ หรือบ้านธาตุจอมศรี หมู่บ้านนี้เดิมเรียกว่า หมู่บ้านท่าจำปา ซึ่งอยู่ทางทิศใต้ของหมู่บ้านธาตุปัจจุบันนี้ บ้านท่าจำปาเดิมคงเป็นหมู่บ้านที่ใหญ่โตพอสมควร เพราะสันนิษฐานได้จากบริเวณที่ตั้งบ้านเดิมนั้น มีวัดวาอุโบสถหลายแห่ง มีการก่อสร้างด้วยอิฐตามคำบอกเล่าว่า พระพุทธรูปมีมากมาย มีทั้งพระพุทธรูปทองคำ ทองเหลือง และทองสัมฤทธิ์ แต่เนื่องจากจีนฮ่อเข้ามารุกรานชาวบ้านจึงนำไปซ่อนโดยนำพระพุทธรูปทองคำไปซ่อนไว้ในแม่น้ำเลย ตรงที่เรียกว่า วังแสนขันปัจจุบันนี้ ส่วนพระพุทธรูปที่สร้างด้วยทอง และทองสัมฤทธิ์ ก็ทาด้วยสีดำให้ดูน่าเกลียด ในจำนวนพระพุทธรูปที่นำไปซ่อนไม่ทันคงเหลืออยู่ 2 องค์ องค์ที่ 1 เรียกตามความนิยมของชาวบ้านเรียกว่า พระเจ้าหน้าดำ เพราะทั้งองค์ดำสนิท พระพุทธรูปองค์นี้ทำด้วยทองสัมฤทธิ์ ปัจจุบันนี้ท่านเจ้าคุณวัดศรีสุทธาวาส (วัดเลยหลง) ได้นำไปประดิษฐานไว้ที่วัดเลยหลง อำเภอเมืองเลย จังหวัดเลย ส่วนอีกองค์หนึ่งนั้น จากคำบอกเล่าว่า ทำด้วยทองหรือทองคำไม่อาจทราบได้ทาด้วยสีดำเช่นเดียวกัน แต่มีลักษณะใสงามกว่าองค์ที่ 1 ที่ขอบพระพักตร์ทาไม่ติดยังคงเป็นทองอยู่ ก่อนหน้าที่ท่านเจ้าคุณจะไปเอานั้น ชาวบ้านได้นำไปซ่อนไว้ในโพรงไม้ เพราะเกรงจะถูกโจรผู้ร้ายขโมยไปเนื่องจากอยู่กลางป่า ครั้นต่อมาชาวบ้านไปหักร้างถางพงใกล้ ๆ ที่ตรงนั้น ไฟป่าจึงลุกลามไปไหม้ต้นไม้ที่นำพระพุทธรูปไปซ่อนไว้ จนถึงรากทำให้ดินยุบพระพุทธรูปจึงตกลงไปอยู่ใต้ดิน คนที่เอาไปซ่อนเกรงจะมีคนไปเห็นอีก จึงเอาใบไม้และดินกลบถมเสีย เข้าใจว่าปัจจุบันนี้คงจะอยู่ที่ใต้ดินนั้นแต่บอกที่แน่นนอนไม่ได้ เพราะคนที่นำไปซ่อนไว้นั้นได้ถึงแก่กรรมไปแล้ว</p>
<p style="font-size: 14px; color: rgb(0, 0, 0); font-family: Geneva, Arial, Helvetica, sans-serif;">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;หลักฐานที่บ่งว่าบ้านท่าจำปา เป็นบ้านที่สร้างมานานและใหญ่โตนั้นนอกจากพระพุทธรูปแล้ว ยังมีสิ่งอื่นๆ อีกเป็นต้นว่า วัตถุ เครื่องใช้ พวกโอ่ง หม้อ มีด ดาบ หอก และเครื่องใช้อื่น ๆ ที่ราษฏรเข้าไปทำไร่แล้วขุดพบ นอกจากนี้ยังมีหมู่บ้านอื่นที่ขึ้นตรงต่อหมู่บ้านท่าจำปา 3 หมู่บ้าน ได้แก่ หมู่บ้านบง อยู่ห่างจากบ้านท่าจำปาไปทางทิศตะวันตกเฉียงใต้ประมาณ 15 เส้น หมู่บ้านหนองเรือ และบ้านนางแม่หม้าย อยู่ห่างประมาณ 100 เส้นไปทางทิศเหนือ หมู่บ้านดังกล่าวนี้ปัจจุบันก็เป็นบ้านร้าง แต่ก็ยังมีหลักฐานปรากฏอยู่เช่นเดียวกัน</p>
<p style="font-size: 14px; color: rgb(0, 0, 0); font-family: Geneva, Arial, Helvetica, sans-serif;">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; สาเหตุที่จะได้อพยพมาตั้งใหม่ให้ชื่อว่าบ้านธาตุจอมศรีนั้น เป็นเพราะหมู่บ้านท่าจำปาเดิมถูกรุกรานจากพวกฮ่อ(จีนฮ่อ) คงจะอยู่ในสมัยของรัชกาลที่ 4 สมเด็จพระจอมเกล้าเจ้าอยู่หัว โดยพวกจีนฮ่อได้ยกทัพหนีทัพไทยมาจากเมืองเชียงคาน (เดิม) เข้าตั้งทัพอยู่ทางด้านทิศเหนือของหมู่บ้านท่าจำปา ซึ่งปัจจุบันเป็นบริเวณบ้านธาตุ หมู่ที่ 1 สิ่งที่เหลือเป็นหลักฐานก็คือ หาดดอนทรายกลางน้ำเลย ชาวบ้านยังคงเรียกชื่ออย่างเดิมว่า &ldquo;หาดฮ่อ&rdquo; และบริเวณบ้านธาตุ หมู่ที่ 1 ก็ยังเรียกชื่ออย่างเดิมว่า บ้านฮ่อ อยู่ การสู้รบนั้นทำกันอยู่นานพอสมควร เพราะหมู่บ้านบง บ้านหนองเรือและบ้านนางแม่หม้าย ได้นำกำลังพลมารวมต่อสู้ร่วมกับหมู่บ้านท่าจำปาไว้ ต่อมาครั้นความทราบถึงพระยาศรีอรรคฮาด เจ้าเมืองเชียงคาน จึงสั่งให้ขุนหลวงไพรวัลย์ (ทุม กาจสงคราม) ซึ่งเป็นต้นตระกูลทุมสงคราม ชาวบ้านธาตุปัจจุบันนี้เป็นแม่ทัพ ยกกำลังจากเมืองเชียงคานมาปราบจีนฮ่อโดยได้มาตั้งทัพอยู่ที่ทุ่งนาหลวง (ทุ่งนาตรงกับบ้านธาตุ หมู่ที่ 12 ปัจจุบัน) การสู้รบได้รบพุ่งกันเป็นเวลานานจนอาวุธจันฮ่อร่อยหลอเกือบหมด บรรดาจีนฮ่อจึงคุมเชลยไปขุดเอาหินเหล็กจากภูเหล็กในปัจจุบัน มาทำเตาถลุงทำเป็นอาวุธสู้กับทัพไทย บริเวณที่สร้างเตาถลุงเหล็กอยู่หลังและในโรงเรียนบ้านธาตุปัจจุบันนี้ ขี้เหล็กและเบ้าหลอมยังมีปรากฏอยู่ ผลการรบทัพไทยเป็นฝ่ายชนะ พวกจีนฮ่อก่อนจะทิ้งหมู่บ้านท่าจำปาได้เผาผลาญทำลายบ้านเรือนชาวบ้าน สิ่งก่อสร้างต่าง ๆ จดหมด สิ่งใดที่นำไปได้ก็ขนหนีไป ที่นำไปไม่ได้ก็คือ พระพุทธรูปทั้ง 2 องค์ดังที่กล่าวมาแล้ว เพราะศักดิ์สิทธิ์จริง ๆ</p>
<p style="font-size: 14px; color: rgb(0, 0, 0); font-family: Geneva, Arial, Helvetica, sans-serif;">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;ฝ่ายแม่ทัพไทยเมื่อรบชนะแล้วก็ไปตรวจดูหมู่บ้านท่าจำปา เห็นว่าถูกทำลายทรุดโทรมมากไม่สามารถจะบรูณะได้ จึงชาวบ้านมาสร้างบ้านใหม่อยู่ใกล้กับบริเวณที่ตั้งทัพทุ่งนาหลวง (ตรงกลางหมู่บ้านปัจจุบันนี้) เมื่อสร้างหมู่บ้านเสร็จแล้ว ท่านแม่ทัพเห็นว่ายังขาดสิ่งที่จะยึดเหนี่ยวทางใจและบำรุงขวัญของชาวบ้านให้กลับมีสภาพดีเป็นปกติอย่างเดิมอยู่ เพราะชาวบ้านพึ่งเสร็จจากการรบพุ่งมาใหม่ ขวัญยังไม่ดีจึงได้ชักพาชาวบ้านสร้างวัดขึ้น ขณะเดียวกันก็ได้สร้างพระธาตุ (ยอดของพระธาตุ) ตบแต่งอย่างงดงาม ประดับประดาด้วยแก้วหลากสี ดูสดใสงดงามมากสามารถมองไกล ๆ เห็นได้อย่างชัดเจน ถึงขนาดว่าช้างม้าวัวควายไม่กล้าเข้าใกล้หรือผ่านได้ต้องใช้ผ้าปิดตา เพราะแสงสะท้อนของแก้วที่ประดับ นายช่างที่นำมาก่อสร้างพระธาตุนี้ทราบจาการบอกเล่าว่า ได้ว่าจ้างมาจากเมืองหลวงพระบาง แต่เนื่องจากการก่อสร้างเป็นไปอย่างเร่งรีบ ฐานของพระธาตุจึงมีมาตรฐานไม่มั่นคง หลายปีต่อมาฐานจึงทรุดพังลงหลายเป็นเหมือนจอมปลวกขนาดใหญ่ อยู่มาจนกระทั่งถึง พ.ศ. 2507 ท่านเจ้าอาวาสวัดพระธาตุเห็นว่าที่วัดคับแคบต้องการขยายเพื่อก่อสร้าง จึงพากันขุดฐานพระธาตุที่เป็นจอมปลวกออก ทราบว่าได้พบกรุที่บรรจุพระพุทธรูปน้อยใหญ่ และสิ่งของโบราณจำนวนมากกรุหนึ่ง พวกที่ขุดพากันแย่งเก็บเอาไปหมด ผู้เขียนขณะนั้นก็ไม่ได้อยู่ที่บ้านธาตุด้วย จึงไม่ทราบว่าปัจจุบันนี้ตกไปอยู่กับผู้ใดบ้าง</p>
